

function bufferToWordArray(buf) {
  var len = buf.length
  var dif = len % 4
  var w = []

  if (!process.browser) {
    for (var i = 0; i < len; i += 4) {
      var n = buf.readUInt32BE(i, true) 
      w.push(n)
    }
    return new WordArray(w, buf.length)
  } else { //bug in browserify / buffer
    for (var i = 0; i < len - dif; i += 4) {
      var n = buf.readUInt32BE(i)
      w.push(n)
    }
    var lw = 0x0
    var off = len - dif
    for (var j = 0; j < dif; j += 1) {
      lw |=  (buf.readUInt8(off + j) << ((3-j)*8))
    }
    if (dif > 0)
      w.push(lw)
    return new WordArray(w, buf.length)
  } 
}

function wordArrayToBuffer(wordArray) {
  var buf = new Buffer(wordArray.words.length * 4)
  for (var i = 0; i < wordArray.words.length; ++i) {
    var w = wordArray.words[i]
    buf.writeUInt32BE(w, i*4, true)
  }
  return buf 
}


